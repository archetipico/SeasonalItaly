export function isInSeason(item: Item, month: number): "peak" | "shoulder" | "off" {
  if (item.peak.includes(month)) return "peak";
  if (item.shoulder?.includes(month)) return "shoulder";
  return "off";
}

export function countByMonth(items: Item[]): number[] {
  const out = new Array(12).fill(0);
  for (const it of items) {
    for (let m = 1; m <= 12; m++) {
      if (isInSeason(it, m) === "peak") out[m - 1]++;
    }
  }
  return out;
}
